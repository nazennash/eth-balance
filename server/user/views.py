logger = logging.getLogger(__name__)

@api_view(["GET"])
@permission_classes([IsAuthenticated])
def get_wallet_balance(request):
    user = request.user
    wallet_address = user.ethereum_wallet

    if not wallet_address:
        logger.error("Ethereum wallet address not found for user %s", user.email)
        return Response({"error": "Ethereum wallet address not found"}, status=status.HTTP_400_BAD_REQUEST)

    try:
        web3 = Web3(Web3.HTTPProvider(settings.INFURA_URL))
        if not web3.is_connected():
            logger.error("Failed to connect to Infura")
            return Response({"error": "Failed to connect to Ethereum network"}, status=status.HTTP_500_INTERNAL_SERVER_ERROR)

        checksum_address = Web3.to_checksum_address(wallet_address)
        balance = web3.eth.get_balance(checksum_address)
        ether_balance = web3.from_wei(balance, 'ether')
        print("Balance",ether_balance)
        logger.info("Fetched balance for user %s: %s ETH", user.email, ether_balance)
        return Response({"balance": ether_balance})
    except Exception as e:
        logger.exception("Error fetching balance for user %s", user.email)
        return Response({"error": str(e)}, status=status.HTTP_500_INTERNAL_SERVER_ERROR)